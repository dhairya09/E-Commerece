    private void insert_into_db(final String name, final String password)
    {
        class ins extends AsyncTask<String,Void,String>
        {


            @Override
            protected String doInBackground(String... strings) {
                String url = strings[0];
                HashMap<String,String> params = new HashMap<String,String>();
                params.put(Config.KEY_EMP_UNAME,name);
                params.put(Config.KEY_EMP_PWD,password);
                RequestHandler rh = new RequestHandler();
                String result = rh.sendPostRequest(url, params);
                return result;
            }

            @Override
            protected void onPostExecute(String res)
            {
                super.onPostExecute(res);
                progressDialog.dismiss();
                Log.d("Result",res);
                if(res.equals("registered"))
                {
                    Toast.makeText(SignupActivity.this, "Successfully Registered", Toast.LENGTH_LONG).show();
                }
                else
                {
                    Toast.makeText(SignupActivity.this,"Invalid username and password",Toast.LENGTH_LONG).show();
                }

            }
        }
        ins ob = new ins();
        ob.execute(Config.URL_ADD_USER);
    }


    public void onSignupSuccess() {
        _signupButton.setEnabled(true);
        setResult(RESULT_OK, null);
        finish();
    }

    public void onSignupFailed() {
        Toast.makeText(getBaseContext(), "Login failed", Toast.LENGTH_LONG).show();

        _signupButton.setEnabled(true);
    }

    public boolean validate() {
        boolean valid = true;

        String name = _nameText.getText().toString();

        String password = _passwordText.getText().toString();

        if (name.isEmpty() || name.length() < 3) {
            _nameText.setError("at least 3 characters");
            valid = false;
        } else {
            _nameText.setError(null);
        }

        /*if (email.isEmpty() || !android.util.Patterns.EMAIL_ADDRESS.matcher(email).matches()) {
            _emailText.setError("enter a valid email address");
            valid = false;
        } else {
            _emailText.setError(null);
        }*/

        if (password.isEmpty() || password.length() < 4 || password.length() > 10) {
            _passwordText.setError("between 4 and 10 alphanumeric characters");
            valid = false;
        } else {
            _passwordText.setError(null);
        }

        return valid;
    }
